            <p>We recommend you include a Bhutanese tsechu (festival) if possible. These are lively events whereby local people, wearing their finest hand-woven clothes, will have come, most on foot, from miles around to gain the blessings of attendance at these rites and to enjoy the camaraderie of family and friends. Dance performances go on all day amidst swirling incense, streaming costumes, ancient, magically charged masks and sacred rites. Major festivals in Bhutan include the Thimphu Tsechu in the fall and Ura Tsechu in the spring. The Jakar Tsechu in autumn and the Trongsa Tsechu in winter are more intimate in scale.
