            <h4>TREKKING</h4>
            <p>Bhutan has raised the bar of environmentally conscious travel with its whole-hearted dedication to preserving its cultural and ecological heritage. Determined to avoid the mistakes made by so many developing countries in the era of mass tourism, the Bhutanese government has instituted strict guidelines that seek to preserve precious resources and sustain traditional ways of life. By staying in tents at prescribed sites along a given route, visitors to Bhutan do not have the usual sweeping economic impact on local farming peoples who are so often transformed into innkeepers and merchants selling trinkets. Nor are whole forests felled to provide wood to cook tourists' meals and heat their rooms, as all fuel on trek is provided exclusively by kerosene.</p>
            <p>The following treks utilize tents, guides and porters. They are yak and pony supported. Lengths are approximate and difficulty ratings are relative.</p>
            <h4>DRUK PATH TREK</h4>
            <p>Four to six days, moderately strenuous. Altitudes reach over 13,000 feet. Start at Paro Dzong, finish in Motithang (near Thimphu). This wonderfully scenic, high-altitude walk accesses remote alpine lakes and yak herders, and boasts views of the Bhutanese Himalayas. Because this trip departs from Paro, it's a great choice as an add-on to the end of your Bhutan trip.</p>
          </div>
          <div class="col col-md-6">
            <h4>BUMTHANG CULTURAL TREK</h4>
            <p>Three days, easy. Start at Toktu Zampa, finish in Mesithang. This trek winds through relatively low-altitude terrain (except for the 11,000-foot climb to Phephe La Pass). The route is scattered with villages and mountain monasteries. It's not as remote as some of the other Bhutan treks, so one is able to access rural Bhutanese culture, meet with villagers and observe the traditional ways of life practiced in the countryside.</p>
            <h4>JHOMOLHARI (CHOMOLHAR) TREK</h4>
            <p>Nine days, strenuous. Altitude reaches 16,000 feet on trek and 13,453 at sleeping point. Start at Drukgyel Dzong (in Paro), finish at Dodina (near Thimphu). This trek through Jigme Dorji National Park, which surrounds sacred Jhomolhari peak, is one of the most popular things to do in Bhutan.</p>
            <h4>SNOWMAN TREK</h4>
            <p>These 25 days are extremely strenuous. Start at Drukgyel Dzong, finish at Sephu. The jewel in the crown of Himalayan trekking, the Snowman is the undisputed classic in the demanding genre of Bhutan treks. Not for the faint of heart, high altitude (over 17,000 feet) and heavy snowfall make this the trek of choice for athletic travelers in excellent physical condition.</p>
