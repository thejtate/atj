            <h4>TREKKING</h4>
            <p>One of the many spectacular things to do in Borneo, trekking can be arranged in the remote Danum Valley, Kinabatangan Wildlife Sanctuary and Gunung Mulu National Park. These can be done as day hikes or overnight treks with tent and guided support.</p>
            <h4>SNORKELING</h4>
            <p>Sipadan is "an untouched piece of art," according to Jacques Cousteau. This tiny island boasts spectacular underwater landscapes, unspoiled marine life and more turtles than you ever dreamt possible. Among the most popular things to do in Borneo, snorkeling is unlimited and just steps away. The accommodations here are rustic-but-clean beach huts. The outstanding food and ambiance combine for a wonderful experience for lovers of the sea.</p>
            <p>Upscale accommodation is available at Mabul Island at the Sipadan Water Village, which offers stilted over-water villas with pleasant air-conditioned rooms. It is just an hour by speedboat from Sipadan Island.</p>
          </div>
          <div class="col col-md-6">
            <h4>EXPEDITION RIVER CRUISING</h4>
            <p>The new luxury barge, RV Orient Pandaw, now operates along Borneo's most remote rivers. The ship has an ultra-shallow draft and can travel to remote areas unreachable by other vessels, let alone overland. There is much to do and see along the river, such as visiting traditional longhouses, exploring river towns and taking jungle treks to spot wildlife like crocodiles, macaques, monitor lizards and hornbills.</p>
            <h4>JHOMOLHARI (CHOMOLHAR) TREK</h4>
            <p>Nine days, strenuous. Altitude reaches 16,000 feet on trek and 13,453 at sleeping point. Start at Drukgyel Dzong (in Paro), finish at Dodina (near Thimphu). This trek through Jigme Dorji National Park, which surrounds sacred Jhomolhari peak, is one of the most popular things to do in Bhutan.</p>
