      <div class="b-cols">
        <div class="row">
          <div class="col col-md-8">
            <p>And then there is the orangutan, or "man of the forest" in Malay. These extraordinary creatures may be seen up close in the wild at places like the Sepilok Orangutan Rehabilitation Center, where orphaned orangutans are conditioned to return to the wild. Founded in 1964, the Sepilok Rehabilitation Centre was created in order to protect orangutans who had been kept in captivity or made homeless due to logging activity, and no trip to Borneo is complete without a visit. The apes that are brought here are rehabilitated to forest life, and there are two daily feeding times for them. Over the years, the center has handled about 80 orangutans, and it now encompasses 15 square miles. In addition to the orangutans, visitors can see Sumatran rhinos and walk on trails through the rainforest for a close-up look at a diverse world of trees, climbing plants, birds and insects.</p>
            <p>Highlights of a Borneo tour could include a jungle river cruise with a silent outboard motor, which allows close access to wild orangutans, macaques and hornbills. Or help release green and hawksbill turtle hatchlings to the sea on Turtle Island. At Kinabatangan Wildlife Sanctuary visit Ox-Bow Lake, to search for hairy-nosed otters, tortoises and the freshwater terrapin. Go out on a nocturnal wildlife jeep safari, to look for the western tarsier and slow loris. Don your mask and flippers and head out into the clear waters off Sipidan to see schools of barracuda that travel in tornado-like formation, greenback and hawksbill turtles, manta rays and huge but harmless whale sharks. Some of our favorite Borneo tours include a classic luxury barge on an expedition up the mighty Rajang River.</p>
            <p>Borneo travel is at once wildly remote and logistically easy, due to a well-developed infrastructure for accessing its natural treasures. Suspended aerial walkways in many areas of the jungle canopy make travel to Borneo an exceptional immersion in wildlife habitat. Eco-tourism in Borneo provides direct financial benefit to its wondrous species, and will help decide the fate of many of Borneo's animals, so don't delay travel to Borneo.</p>
