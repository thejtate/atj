            <h4>Khao Yai National Park</h4>
            <p>A pleasant 2.5-hour drive from Bangkok leads you to the dense forest, rugged mountains and abundant wildlife of Thailand's oldest national park. The park covers 2,172 square km and includes one of the largest intact monsoon forests in mainland Asia. Some 200 to 300 wild elephants inhabit the park, along with sambar deer, barking deer, gaur, wild pigs, Malayan sun bears, and various gibbons and macaques. The park is best seen on foot, crossing narrow suspension bridges and trekking remote trails in search of hidden waterfalls and mysterious caves.</p>
            <h4>Northern Hill Tribes</h4>
            <p>Many ATJ clients report that trekking between the various hill tribe villages of northern Thailand was the most memorable part of their trip. Before you depart on your trek be sure to visit the Hill Tribe Ethnographic Museum (about a half-hour drive from Chiang Mai) for an excellent introduction to the various tribes. Then, don some sturdy hiking boots and prepare to enter this remote and beautiful area peopled by Karen, Akha, Lahu, Lisu and Hmong tribespeople of Tibetan, Burmese and Chinese origin who have migrated and resolved to maintain their traditional ways. </p>
