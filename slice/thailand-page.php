          <p>This is a very different elephant experience than most tourists have in Thailand. Patara is recognized for putting elephant wellbeing first and teaching visitors about the nuances of caring for these highly intelligent creatures. Start with a briefing on the history of the domestic elephant in Thailand before beginning your mahout (elephant handler) training session. During the course of the day, you will learn about elephant farm management and elephant breeding programs. After your briefing, walk into the jungle to find your elephants in the bush. Learn how to approach and be near elephants and observe their temperament. Then spend some time feeding and taking care of your elephant ending with a bath in the river (for both your elephant and yourself!)</p>
