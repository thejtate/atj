            <p>Cut dried chiles into 1/4-inch pieces with kitchen shears and soak in warm water until softened, about 20 minutes. Drain well in a sieve.</p>
            <p>While chiles soak, toast coriander in a dry small heavy skillet over moderate heat, shaking skillet, until fragrant, 3 to 4 minutes, then cool. Thinly slice lower 6 inches of lemongrass stalks and finely chop.</p>
            <p>Finely grind coriander and peppercorns with mortar and pestle (or in mini food processor), about 2 minutes, then toss together with lemongrass, galangal, lime leaves, cilantro, shallot, garlic, fresh chiles, and soaked dried chiles in a bowl. Pound mixture in 3 batches with mortar and pestle until a fairly smooth paste is formed, 8 to 10 minutes per batch, transferring to cleaned bowl. (If using food processor, add about 1 1/2 tablespoons water per batch.) Return all of curry paste to mortar, then add shrimp paste and salt and pound (or pulse) until combined well, about 1 minute.</p>
