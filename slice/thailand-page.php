            <h4>Chiang Mai</h4>
            <p>Well known for its superb temples and handicraft shops, cool nights and gentle culture, Chiang Mai has wooed travelers for years. An important cultural and religious center for seven centuries, Chiang Mai was once an independent kingdom whose lands were sought by the Burmese, Thais and Laoatians. Don’t miss Wat Phra That Doi Suthep, situated high on a hill above the city, or a visit to the bustling Night Bazaar to pick up souvenirs of silk, lacquer ware, silver, paper, parasols and celadon pottery.</p>
            <h4>Krabi Province</h4>
            <p>This area of southern Thailand boasts one of the most stunning landscapes in the country—its beaches and dramatic karst islands are picture perfect. The harbor town of Krabi serves as a convenient jumping-off point for the area. Head to Koh Phi Phi for a blissful beach or take a snorkeling day trip to nearby Chicken Island. Or, to really get away from it all, charter a private sailboat to explore the islands of Pha Nga Bay and access gorgeous beaches and islands inaccessible to other travelers.</p>
