                    <p>17 to 20 (2- to 3-inch-long) prik haeng (dried hot red chiles), halved and seeds discarded</p>
                    <p>4 teaspoons coriander seeds</p>
                    <p>2 fresh lemongrass stalks, 1 or 2 outer leaves discarded (or use reserved bottoms from iced lemongrass tea, page 160)</p>
                    <p>1 teaspoon whole black peppercorns</p>
                    <p>4 teaspoons finely chopped peeled fresh or thawed frozen greater galangal (sometimes called kha)</p>
                    <p>6 (4-inch-long) fresh or frozen Kaffir lime leaves (sometimes called bai makroot), finely chopped</p>
                  </div>
                  <div class="col col-sm-6">
                    <p>2 tablespoons chopped fresh cilantro roots or stems</p>
                    <p>5 small shallots, chopped (6 tablespoons)</p>
                    <p>1/4 cup chopped garlic</p>
                    <p>15 to 20 (1-inch-long) red prik kii noo (fresh bird's-eye chiles) or serrano chiles, finely chopped</p>
                    <p>2 teaspoons ga-pi (Thai shrimp paste)</p>
                    <p>1/2 teaspoon salt</p>
                    <p>Special equipment: a large (2-cup) mortar and pestle (preferably granite) or a mini food processor</p>
