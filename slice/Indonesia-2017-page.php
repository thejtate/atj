          <h4>See Orangutans in the Wild (Camp Leakey, Kalimantan, Indonesia)</h4>
          <p>Board a traditional klotok (a flat-bottomed river boat) in Pangkalanbun and cruise upriver to Camp Leakey. This research center was founded in 1971 by Dr. Birute Galdikas for the study and conservation of orangutans. Like Jane Goodall's work with chimpanzees and Diane Fossey's with gorillas, Dr. Galdikas' hands-on approach to researching orangutans—she has lived for decades in this jungle—has resulted in a profoundly influential body of work. Likely encounters include several of the resident ex-captive orangutans that roam within the grounds of the research camp as well as visits to several feeding sites.</p>
        </div>
        <div class="item">
          <h4>Attend a Village Feast Held in Your Honor (Bulian, Bali, Indonesia)</h4>
          <p>Drive past rice terraces and active volcanoes to the remote village of Bulian as the special guests of long-time friend of ATJ, Komang. Upon arrival, drop by the school to see the young boys and girls of the village learning the traditional Balinese dances and the traditional art of Balinese music. Then accompany Komang to his home for an extravagant home-cooked dinner and a unique music and dance performance (you’ll be expected to join in!). This experience is exclusive to ATJ.</p>
        </div>
        <div class="item">
          <h4>Dine like Royalty, with Royalty (Sanur, Bali, Indonesia)</h4>
          <p>Malaika (a princess of one of Bali’s royal families) welcomes you to her villa with a shower of flower petals. A private table is set up poolside in her beautifully decorated courtyard (flowers floating in the pool, lanterns, candles). Staff from her nearby restaurant help out with the cooking and serving as Malaika guides you through the meal and provides delightful conversation. Food is seriously amazing—combo of Balinese classics and innovative raw cuisine.</p>
        </div>
        <div class="item">
          <h4>See a Different Side of Bali (Muntigunung, Bali, Indonesia)</h4>
          <p>For a real off-the-beaten-path experience in Bali, drive up the steep 4WD road above Lake Batur and set out on a day trek through the very remote region of Muntigunung. Walking through this area, still incredibly undeveloped, shows whole different side of Bali from what most travelers see. Sleepy hamlets, some still inaccessible by road, are home to friendly locals growing produce to sell at the local market. This area is so rarely visited that you will likely be as much an attraction to the people you meet as visa versa. The hike ends by visiting several projects initiated by an NGO in the area to give the very poor villagers access to water and economic opportunities (rain-water collection centers, basket weaving workshop, hammock-making workshop, dried-mango and cashew processing facility).</p>
