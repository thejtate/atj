            <h4>Punakha</h4>
            <p>Despite serving as the nation’s winter capital for 300 hundred years, Punakha is a small, sleepy town. The spiritual center of the town is the great Punakha Dzong, which stands at the confluence of the Mo Chhu and Phu Chhu (Female and Male Rivers). The dzong is still the winter residence of the Je Khenpo and the state’s monastic community. A great festival is held at the dzong each year to celebrate the end of winter; the main event of the festival is the unveiling of a giant appliqued thangka, or thongdrel.</p>

            <h4>The Phobjika Valley</h4>
            <p>One of Bhutan’s designated conservation areas, this valley is home to many of Bhutan’s large mammals, birds and unique flora. The broad glacial valley is covered with several marshes, which are taken over each winter by large flocks of the rare black-necked crane. </p>

            <h4>Jakar</h4>
            <p>The capital of the Bumthang Region, this town serves as a base to most travelers who come to enjoy the beauty of Bumthang’s rolling hills and valleys. Due to its convenient location on the east-west highway, Jakar is slowly transforming from a small village to a commercial center. The Jakar Dzong, or "Fortress of the White Bird," looks over the valley from the top of a small hill; legend relates that the site and name were chosen when a white bird landed on the dzong’s present site and refused to move. Though the dzong has been renovated and repaired several times, the original construction dates back to 1549. </p>

            <h4>Mebartsho (aka Burning Lake)</h4>
            <p>This sacred lake is Bhutan’s holiest pilgrimage site. It was at this gorge that Pema Lingpa found sacred Buddhist treasures. To prove the divine nature of his treasures and of his calling to propagate Buddhism, Pema Lingpa jumped into the lake with a burning oil lamp. When he resurfaced, the lamp was still burning, proving that he was not a demon but a spiritual son of Guru Rinpoche.</p>
          </div>
          <div class="col col-md-6">
            <h4>Ducha La Pass</h4>
            <p>(10,023 feet) from Thimphu to Punakha is stunning, as the entire Bhutan Himalaya can be seen from the summit on a clear day. From Ducha La the road drops sharply through rhododendron and magnolia forests into the lush semi-tropical forests of the Punakha Valley.</p>

            <h4>Trongsa</h4>
            <p>Located in the middle of Bhutan, the town of Trongsa is the religious and administrative seat of the region. The impressive Trongsa Dzong stands high above the surrounding valleys. The dzong is located directly above the historical trade routes of central Bhutan; hence it was easy for the leaders of Trongsa Dzong, with its commanding views, to control all east-west traffic for several centuries. The dzong has deep connections to the royal family; the first king ruled from the Trongsa Dzong and even today the Crown Prince of Bhutan holds the hereditary title of Trongsa Penlop (governor). </p>

            <h4>Tamshing Goemba</h4>
            <p>Built in the early 16th century, this monastery contains many stunning paintings, some of which are the oldest extant paintings within Bhutan! The goemba is one of the last remaining monasteries to propagate the teachings of the great terton, Pema Lingpa; in fact, one of the goemba’s main relics is the piece of chain-mail armor purportedly made by the great terton himself.</p>

            <h4>Ura</h4>
            <p>The sheep-herding communities of the Ura Valley have witnessed unrivaled prosperity in the last twenty years due to the construction of the east-west highway. However, despite the improvements in daily life, the traditions and cultures of the Ura people have been left firmly intact. Here communities are nestled closely together and connected with stone walkways and paths. The main village of the valley is Ura, home of a new temple and monastery.</p>
