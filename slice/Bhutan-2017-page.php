              <p>The national capital of Bhutan, Thimphu is larger and slightly more developed than other places in Bhutan, but with a population of only about 104,000 and no traffic lights, it is hardly urban sprawl. All buildings within the town make use of Bhutanese architectural styles and materials, giving the modern capital a very traditional character. </p>
