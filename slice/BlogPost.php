                <h5>5. ritigala</h5>
                <a class="link-view" href="theme/images/tmp/tmp-blog-6.jpg"></a>
              </div>
              <div class="body">
                <p>Ritigala is close to Anuradhapura but overlooked. Their loss is your gain as the Ritigala Forest Monastery and archaeological site is at the top of my list of off the beaten path destinations in Sri Lanka. The monastery itself is shrouded in surrounded by legend, myth and mystery and still used by Sri Lankan forest monks in their spiritual path to enlightenment. The archaeological ruins echo the splendor of those found ones in Anuradhapura but display a markedly different architectural style. As you explore, you might just happen upon a herd of wild elephants peeking out from the surrounding jungle.</p>
