                <img src="theme/images/tmp/tmp-blog-2.jpg" alt=""/>
                <h5>1. Rummassala</h5>
                <a class="link-view" href="theme/images/tmp/tmp-blog-2.jpg"></a>
              </div>
              <div class="body">
                <p>"Ru-mas-sala" translates to "Beautiful Rock." It's a fitting description of this stunning plot of elevated land, visible from Galle Fort. The Hindu epic The Ramayana reveals a beautiful folklore surrounding Rummassala. Legend tells that the rock is a chunk of the Himalayan Mountains which fell off while the Monkey God, Hanuman, was transporting it to bring back to his brother, the Prince, to save his life as the mountain grew special medicinal herbs. Science also attests to the uniqueness of the land. It's lushness it attributed to an abundance of minerals as the result of an ancient meteor strike. To best experience this mystical locale, hike from Unawatuna beach through Rummassala to secluded and beautiful Jungle Beach. Here you'll be treated a panoramic view of Galle Bay and the Indian Ocean, a great place to enjoy a sundowner. Rummassala is easily accessible from Galle, though it feels worlds away.</p>
              </div>
              <div class="photo">
                <img src="theme/images/tmp/tmp-blog-3.jpg" alt=""/>
                <h5>2. Mannar</h5>
                <a class="link-view" href="theme/images/tmp/tmp-blog-3.jpg"></a>
              </div>
              <div class="body">
                <p>Mannar is a coastal sub-island in the Northwest district of Sri Lanka, with a Tamil/ Muslim majority and the perfect place to experience Sri Lanka's encouraging post civil war revival. The rough and tumble work-a-day city offers a rich contrast to its numerous historic attractions like the Thiruketheeswaran Kovil, Adam’s Grave, Adam’s Bridge, Star Fortress and Doric House. If you need a break from the bustle, a nearby biosphere attracts endemic and migrant birds. It's the perfect setting for an afternoon stroll and a must for birders.</p>
              </div>
              <div class="photo">
                <img src="theme/images/tmp/tmp-blog-4.jpg" alt=""/>
                <h5>3. Jaffna</h5>
                <a class="link-view" href="theme/images/tmp/tmp-blog-4.jpg"></a>
              </div>
              <div class="body">
                <p>Jaffna was once the main city of LTTE-controlled northern Sri Lanka under the civil war. Though pockmarked by the recent past, resilient Jaffna is recovering and eagerly welcoming back tourists. This ancient city has held a place of prominence on the island for thousands of years owing to its close proximity to southern India. It was through Jaffna that Indian kings first conquered the island, followed by Western colonists and plenty of contemporary Tamil immigrants as well. Accordingly, Jaffna boasts a unique and especially rich lifestyle and culture. Once of the best ways to touch the pulse of this diverse city is through its gastronomic delights as the Jaffna Style Crab Curry, Jaffna Style Mutton Curry or, my personal favorite, Jaffna Coffee, a warm and creamy coffee spiced with ginger and coriander.</p>
              </div>
              <div class="photo">
                <img src="theme/images/tmp/tmp-blog-5.jpg" alt=""/>
                <h5>4. Gal Oya national park</h5>
                <a class="link-view" href="theme/images/tmp/tmp-blog-5.jpg"></a>
              </div>
              <div class="body">
                <p>Gal Oya National Park hosts my favorite wildlife experience in Sri Lanka. And, even better,  you will be hard pressed to find another tourist to spoil the view. Gal Oya's landscape is a coastal wilderness of linked islands best explored by boat. Set sail on one of Sri Lanka’s largest “tanks” (manmade reservoirs) and watch herds of endangered Asian Elephants play, bathe and feed on the shoreline. If you're lucky, you might even glimpse elephants swimming from one island to another! For a different perspective on the area's flora and fauna, go for a hike with a member of the indigenous Vedda people and learn about their hunter-gatherer, forest-dwelling ways.</p>
              </div>
