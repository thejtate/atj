            <h4>BEIJING/LHASA BY TRAIN</h4>
            <p>In 2006 the Chinese completed the Qinhai Tibet Railway, linking Beijing, Chengdu, Xi'an and Xining with Lhasa. This engineering marvel travels across the high Tibetan Plateau, through densely forested mountains above Xining. There are two different trains plying the tracks, a modest local model and the luxurious Tangula, which can be compared to the Eastern & Orient Express. Tangula's custom-built trains feature relaxed elegance, state-of-the-art technology, personal entertainment systems and onboard oxygen facilities allowing you to travel in comfort at altitudes of over 16,000 feet. Private soft sleeper cabins feature 24-hour attendant service. Each berth sleeps four passengers (two upper and two lower twin berths). Travel from Xining to Lhasa (or visa versa) takes 24 hours and is the most scenic part of the journey. One of the many extraordinary things to do in China and a fantastic way to see the country's varied landscapes, the entire Beijing to Lhasa route is a five-day/four-night journey, while the Lhasa to Beijing direction is a four-day/three-night journey.</p>
            <h4>FOUR HOLY MOUNTAINS</h4>
            <p>Considered holy sites by Taoists and Buddhists and used for imperial ancestral sacrifices, these four holy mountains of Tai Shan, Huang Shan, Qingcheng Shan and Emei Shan combine stunning natural scenery with religious and political history. Follow in imperial footsteps at Tai Shan in Shandong, climb through the "cloud seas" of Huang Shan in Anhui, linger at the many Buddhist temples perched on Emei Shan in Sichuan or explore China's first Taoist temples atop Qingcheng Shan in Sichuan.</p>
          </div>
          <div class="col col-md-6">
            <h4>RIVER TRIPS</h4>
            <p>In Yangshuo take a bamboo raft trip on a beautiful tributary of the Li River. A cruise on the Yangzi is of course a classic of the genre and one of the more popular China activities amongst visitors.</p>
            <h4>FESTIVALS</h4>
            <p>The Chinese are justifiably famous the world over for their festival pageantry. Ask us to budget in time to attend a celebration. Yunnan's Water-Splashing Festival in April or the Dragon Boat Festival in June are among our favorite festivals in China.</p>
            <h4>TRANS-SIBERIAN RAILROAD</h4>
            <p>Four to six days, moderately strenuous. Altitudes reach over 13,000 feet. Start at Paro Dzong, finish in Motithang (near Thimphu). This wonderfully scenic, high-altitude walk accesses remote alpine lakes and yak herders, and boasts views of the Bhutanese Himalayas. Because this trip departs from Paro, it's a great choice as an add-on to the end of your Bhutan trip.</p>
